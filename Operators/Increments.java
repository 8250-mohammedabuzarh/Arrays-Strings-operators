package Operators;

public class Increments {
    public static void main(String[] args)
    {
        int a=10;
        int b= a++;
        System.out.println(b);
        int c=10;
        int d= c--;
        System.out.println(d);
        int e=10;
        int f= ++e;
        System.out.println(f);
        int g=10;
        int h= --g;
        System.out.println(h);
    }
}
