package Operators;

public class Divisability {
    public static void main(String[] args)
    {
        int n=35;
        if(n%5==0 && n%7==0)
        {
            System.out.println("Divisible by 5 and 7");
        }
        else{
            System.out.println("Not Divisible by 5 and 7");
        }
    }
}
